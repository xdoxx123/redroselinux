#include <stdio.h>
#include <unistd.h>
#include <sys/reboot.h>
#include "tui.c"
#include "backend.c"
#include "postinstgen.c"

// this file is the main file of the installer.
// it exports the other functions from tui.c and
// backend.c. got nothing else to say
